import numpy as np

# PythonのリストからNumPy配列を作成
arr = np.array([1, 2, 3])
# 出力: [1 2 3]

# 0から9までの整数を生成
arr2 = np.arange(0, 10, 2)
# 出力: [0 2 4 6 8]

# 0から1までを5分割する
arr3 = np.linspace(0, 1, 5)
# 出力: [0.   0.25 0.5  0.75 1.  ]

# 2x2の全ての要素が0の配列を生成
arr4 = np.zeros((2, 2))
# 出力: [[0. 0.]
#        [0. 0.]]

# 2x2の全ての要素が1の配列を生成
arr5 = np.ones((2, 2))
# 出力: [[1. 1.]
#        [1. 1.]]

# 3x2の0から1までのランダムな数値を含む配列を生成
arr6 = np.random.rand(3, 2)
# 出力: (例) [[0.12 0.34]
#             [0.56 0.78]
#             [0.91 0.22]]

# 配列の全要素の和を計算
sum_arr = np.sum([1, 2, 3])
# 出力: 6

# 配列の全要素の平均値を計算
mean_arr = np.mean([1, 2, 3])
# 出力: 2.0

# 1次元配列を3x2の2次元配列に形状変更
reshaped_arr = np.arange(6).reshape((3, 2))
# 出力: [[0 1]
#        [2 3]
#        [4 5]]

# ベクトルや行列の内積を計算
dot_result = np.dot([1, 2], [3, 4])
# 出力: 11
